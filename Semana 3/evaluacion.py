# Algortimo                               |   T(n)   |  O(n)  |
def es_primo(numero):
    if(numero <= 1):                    # |__________|________|
        return False                    # |__________|________|
    else:                               # |__________|________|
        cont = 0                        # |__________|________|
        for i in range(2,numero+1):     # |__________|________|
            if(numero % i == 0):        # |__________|________|
                cont += 1               # |__________|________|
        if(cont == 1):                  # |__________|________|
            return True                 # |__________|________|
        else:                           # |__________|________|
            return False                # |__________|________|