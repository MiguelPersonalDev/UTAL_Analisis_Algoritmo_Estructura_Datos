def fibonacci(n):
    n0 = 0                          # |__________|________|
    n1 = 1                          # |__________|________|
    fib = 0                         # |__________|________|
    if n == 0 or n == 1:            # |__________|________|
        fib = n                     # |__________|________|
    else:                           # |__________|________|
        i = 2                       # |__________|________|
        while i <= n:               # |__________|________|
            fib = n0 + n1           # |__________|________|
            n0 = n1                 # |__________|________|
            n1 = fib                # |__________|________|
            i += 1                  # |__________|________|
    return fib                      # |__________|________|


